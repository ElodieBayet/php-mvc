<section>
    <h2>Description</h2>
    <p>Les patrons de conception ont été formellement reconnus en 1994 à la suite de la parution du livre <em>Design Patterns: Elements of Reusable Software</em>, co-écrit par quatre auteurs : Gamma, Helm, Johnson et Vlissides <span class="light">– Gang of Four › “la bande des quatre”</span>. Ce livre, devenu un best-seller, décrit <strong>vingt-trois “patrons GoF”</strong> et comment s'en servir.</p>
    <h3>Familles selon leur utilisation</h3>
    <div class="grid md">
        <div class="col-50">
            <h4>Patrons de Création</h4>
            <p>Ils définissent comment faire l'instanciation et la configuration des classes et des objets.</p>
            <ul class="listline apart">
                <li><span class="light">Liste :</span></li>
                <li><strong>Singleton</strong></li>
                <li><strong>Prototype</strong></li>
                <li><strong>Fabrique</strong></li>
                <li><strong>abstraite</strong></li>
                <li><strong>Monteur</strong></li>
            </ul>
        </div>
        <div class="col-50">
            <h4>Patrons de Structure</h4>
            <p>Ils définissent comment organiser les classes d'un programme dans une structure plus large <span class="light">– séparant l'interface de l'implémentation</span>.</p>
            <ul class="listline apart">
                <li><span class="light">Liste :</span></li>
                <li><strong>Pont</strong></li>
                <li><strong>Façade</strong></li>
                <li><strong>Adaptateur</strong></li>
                <li><strong>Objet composite</strong></li>
                <li><strong>Proxy</strong></li>
                <li><strong>Poids-mouche</strong></li>
                <li><strong>Décorateur</strong></li>
            </ul>
        </div>
        <div class="col-50">
            <h4>Patrons de Comportement</h4>
            <p>Ils définissent comment organiser les objets pour que ceux-ci collaborent <span class="light">– distribution des responsabilités –</span> et expliquent le fonctionnement des algorithmes impliqués.</p>
            <ul class="listline apart">
                <li><span class="light">Liste :</span></li>
                <li><strong>Chaîne de responsabilité</strong></li>
                <li><strong>Commande</strong></li>
                <li><strong>Interpréteur</strong></li>
                <li><strong>Itérateur</strong></li>
                <li><strong>Médiateur</strong></li>
                <li><strong>Mémento</strong></li>
                <li><strong>Observateur</strong></li>
                <li><strong>État</strong></li>
                <li><strong>Stratégie</strong></li>
                <li><strong>Patron de méthode</strong></li>
                <li><strong>Visiteur</strong></li>
            </ul>
        </div>
    </div>
