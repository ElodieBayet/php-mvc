    <h2>Description de “PHP-MVC”</h2>
    <div class="grid md">
        <div class="col-50">
            <h3>Projet</h3>
            <p>Cette application est un exemple de développement d'un site web en <strong>vanilla PHP</strong> à travers l'architecture <strong>Model-View-Controller</strong> et le paradigme <strong>Orienté Objet</strong>.</p>
            <h4>Objectifs</h4>
            <ul class="list">
                <li>Apprendre la résolution d'un système de routage en PHP</li>
                <li>Découvrir l'architecture <strong>Model-View-Controller</strong> et la <strong>Programmation Orientée Objets</strong></li>
                <li>Observer une structure et une organisation de fichiers pertinentes</li>
            </ul>
        </div>
        <div class="col-50">
            <h3>Thématique</h3>
            <p>Le thème employé est celui des <strong>Design Patterns</strong>. Cette application étant une simple démonstration de développement, elle n'a pas pour vocation de présenter un contenu absolument complet, et peut se montrer très approximative sur certains points.</p> 
            <h4>Sources</h4>
            <p>Les sources principales <span class="light">– et uniques –</span> des informations présentées ne sont autres que la page <a href="https://fr.wikibooks.org/wiki/Patrons_de_conception" title="Aller sur le site" target="_blank" rel="noreferer">Wikibooks : Patrons de Conception</a> et la page <a href="https://fr.wikipedia.org/wiki/Patron_de_conception" title="Aller sur le site" target="_blank" rel="noreferer">Wikipédia : Patron de conception</a>.</p>
        </div>
    </div>
    <div class="grid md">
        <div class="col-50">
            <h3>Étude</h3>
            <p>En Orienté objet, <em>tout</em> est objet. Et selon les principes de l'architecture MVC, un contrôleur capte une "demande" utilisateur, opère un traitement sur le modèle – si lieu – puis notifie la vue qui est alors "mise à jour".</p>
            <p>La requête de chaque page est détournée vers un <em>router</em> puis elle abouti sur un contrôleur unique. Lequel charge une vue, le layout de la page, et construit une réponse à renvoyer au <em>client</em>.</p>
