    <h2>Classification</h2>
    <p>Il y a différentes façons de classer les différents patrons de conception. Ce livre présente les plus connus, classés en fonction de leurs auteurs, puis, si possible, classés par catégorie fonctionnelle.</p>
    <h3>Ensemble de patrons de conception</h3>
    <div class="grid md">
        <div class="col-50">
            <h4>Les patrons du “Gang of Four”</h4>
            <p>Ou <em>GoF</em> d'après Erich Gamma, Richard Helm, Ralph Johnson et John Vlissides, décrits dans leur livre “Design Patterns - Elements of Reusable Object-Oriented Software” en 1995. Les patrons de conception tirent leur origine des travaux de l'architecte Christopher Alexander dans les années 70.</p>
        </div>
        <div class="col-50">
            <h4>Les patrons GRASP</h4>    
            <p>Sont des patrons créés par Craig Larman, qui décrivent des règles pour affecter les responsabilités aux classes d'un programme orienté objets pendant la conception, en liaison avec la méthode de conception BCE <span class="light">– pour “Boundary Control Entity”, c'est à dire MVC, “Modèle Vue Contrôleur” –</span>.</p>
        </div>
        <div class="col-50">
            <h4>Les patrons d'entreprise</h4>
            <p><em>Enterprise Design Pattern</em>, créés par Martin Fowler, décrivent des solutions à des problèmes courants dans les applications professionnelles. Par exemple, des patrons de couplage entre un modèle objet et une base de donnée relationnelle.</p>
        </div>
        <div class="col-50">
            <h4>D'autres patrons</h4>
            <p>créés par divers auteurs existent et décrivent des solutions à des problèmes différents de ceux vus précédemment.</p>
        </div>
    </div>
