    <h2>Description</h2>
    <p>En génie logiciel, un patron de conception <span class="light">– Design pattern –</span> est une <strong>solution générique d'implémentation</strong> répondant à un problème spécifique. En général, un patron de conception décrit <strong>une structure de classes utilisant des interfaces</strong>, et s'applique donc <strong>à des développements logiciels utilisant la programmation orientée objet</strong>.</p>
    <h3>Étude des Design Patterns</h3>
    <p>Un livre <a href="https://fr.wikibooks.org/wiki/Patrons_de_conception" title="Aller sur le site" target="_blank" rel="noreferer">Wikibook</a> est disponible librement pour apprendre les patrons de conception. Ce livre a pour but de faire découvrir les patrons de conception aux programmeurs débutants et s'adresse également aux programmeurs confirmés en dressant l'inventaire des patrons de conception les plus connus.</p>
    <p>Les pré-requis recommandés sont les suivants :</p>
    <ul class="list">
        <li>Connaître le vocabulaire de la programmation objet, employé intensivement pour décrire les patrons de conception,</li>
        <li>Avoir des notions de modélisation UML, notamment sur le diagramme de classes.</li>
    </ul>
